OUTPUT_FILE = os.path.join(os.path.dirname(__file__), "morning_meditation_long.mp3")

async def generate():
    print("Generating 10-minute ORGANIC NATURE meditation (Single Request)...")
    
    # Объединяем весь текст в одну строку с паузами
    full_text = " \n ".join(SCRIPT)
    
    # Проверка длины (лимит OpenAI ~4096 символов)
    print(f"Total characters: {len(full_text)}")
    if len(full_text) > 4000:
        print("WARNING: Text might be too long for single request. Truncating slightly if needed.")

    if os.path.exists(OUTPUT_FILE):
        os.remove(OUTPUT_FILE)

    try:
        response = await client.audio.speech.create(
            model="tts-1",
            voice="nova",
            input=full_text,
            speed=0.85 
        )
        
        # Сохраняем сразу весь файл
        response.stream_to_file(OUTPUT_FILE)
        print(f"\nDONE! File saved: {OUTPUT_FILE}")
        
    except Exception as e:
        print(f"Error: {e}")

if __name__ == "__main__":
    asyncio.run(generate())
